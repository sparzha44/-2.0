from datetime import datetime


def year(request):
    """
    Контекст-процессор, добавляющий текущий год в контекст всех шаблонов.
    
    Это функция, которая:
    1. Принимает на вход объект request
    2. Возвращает словарь с переменными для шаблонов
    
    В шаблонах будет доступна переменная {{ year }}
    """
    return {
        'year': datetime.now().year
    }
